#Membership Operators
#Membership operators are used to test if a sequence is presented in an objet

#IN
numbers=[1,2,3,4]
print(2 in numbers)

colors=['blue', 'red', 'yellow']
print('green' in colors)

#NOT IN
numbers = [1,2,3,4]
print(2 not in numbers)

colors = ['blue', 'red', 'yellow']
print('green' not in colors)

